    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        if(enterprise instanceof PHSEnterpise){
            organizationJComboBox.addItem(Organization.Type.AssetMgt);
            organizationJComboBox.addItem(Organization.Type.Finace);
        }else if(enterprise instanceof HospitalEnterpise){
            organizationJComboBox.addItem(Organization.Type.MedStaff);
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        model.setRowCount(0);
        
        for (Organization organization : enterprise.getOrganazDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
